package Execrises;

public class Loop {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }

        int a = 1;
        while (a <= 10) {
            System.out.print(a);
            a++;
        }

        int b = 1;
        do {
            System.out.println(b);
            b++;
        } while (b <= 10);

    }
}
